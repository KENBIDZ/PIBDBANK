
CREATE TABLE Pengguna (
    NIK VARCHAR(16) PRIMARY KEY,
    Nama_Pengguna VARCHAR(100),
    Alamat_Pengguna TEXT,
    NoHP_Pengguna VARCHAR(15),
    Email_Pengguna VARCHAR(100),
    Password_Pengguna VARCHAR(100),
    TanggalLahir_Pengguna DATE,
    JenisKelamin_Pengguna VARCHAR(20),
    Status_Pengguna VARCHAR(20)
);

CREATE TABLE Rekening (
    No_Rekening VARCHAR(20) PRIMARY KEY,
    Saldo_Rekening DECIMAL(15,2),
    Status_Rekening VARCHAR(20),
    Jenis_Rekening VARCHAR(20),
    NIK VARCHAR(16),
    FOREIGN KEY (NIK) REFERENCES Pengguna(NIK)
);

CREATE TABLE Transaksi (
    ID_Transaksi VARCHAR(20) PRIMARY KEY,
    Tanggal_Transaski DATE,
    Jumlah_Transaksi DECIMAL(15,2),
    Total_Debit DECIMAL(15,2),
    Total_Kredit DECIMAL(15,2),
    Status_Transaksi VARCHAR(20),
    No_Rekening VARCHAR(20),
    FOREIGN KEY (No_Rekening) REFERENCES Rekening(No_Rekening)
);

CREATE TABLE Investasi (
    No_Investasi VARCHAR(20) PRIMARY KEY,
    status_investasi VARCHAR(10),
    jenis_investasi VARCHAR(20),
    ID_Transaksi VARCHAR(20),
    FOREIGN KEY (ID_Transaksi) REFERENCES Transaksi(ID_Transaksi)
);

CREATE TABLE Transfer (
    No_Transfer VARCHAR(20) PRIMARY KEY,
    NoRek_Penerima VARCHAR(20),
    Biaya_Kirim INT,
    Keterangan TEXT,
    Sumber_dana VARCHAR(50),
    FOREIGN KEY (No_Transfer) REFERENCES Transaksi(ID_Transaksi)
);

CREATE TABLE Penjualan_Investasi (
    No_Penjualan VARCHAR(20) PRIMARY KEY,
    Tanggal_Jual DATE,
    Harga_Jual DECIMAL(15,2),
    Total_Jual DECIMAL(15,2),
    Status_Jual VARCHAR(20),
    No_Investasi VARCHAR(20),
    FOREIGN KEY (No_Investasi) REFERENCES Investasi(No_Investasi)
);

CREATE TABLE Pembayaran (
    No_Pembayaran VARCHAR(20) PRIMARY KEY,
    Sumber_dana VARCHAR(50),
    Waktu_Bayar DATE,
    Pajak DECIMAL(15,2),
    Total_Bayar DECIMAL(15,2),
    Status_bayar VARCHAR(20),
    FOREIGN KEY (No_Pembayaran) REFERENCES Transaksi(ID_Transaksi)
);

CREATE TABLE Jenis_Pembayaran (
    ID_JenisPembayaran VARCHAR(20) PRIMARY KEY,
    Nama_JenisPembayaran VARCHAR(50),
    No_Pembayaran VARCHAR(20),
    FOREIGN KEY (No_Pembayaran) REFERENCES Pembayaran(No_Pembayaran)
);

